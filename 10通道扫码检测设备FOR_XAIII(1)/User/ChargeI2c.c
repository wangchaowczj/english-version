//==================================================================================================
//| 函数名称 | I2C_WaitAck 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | 等待应答信号
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 1有ACK
//|          | 0无ACK
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
unsigned char Charge_I2C_WaitAck(void)  //
{
	Charge_I2C_Delay(3);
	CHARGE_I2C_SCL_L;
	Charge_I2C_Delay(3);
	CHARGE_I2C_SDA_H; 

	Charge_I2C_Delay(3);
	CHARGE_I2C_SCL_H;
	Charge_I2C_Delay(3);
	if(CHARGE_I2C_SDA_READ())
	{ 
		CHARGE_I2C_SCL_L;
		return 0;
	}	 
	CHARGE_I2C_SCL_L;
	return 1;
}
//==================================================================================================
//| 函数名称 | I2C_SendByte 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | 发送1个字节数据
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | SendByte 要数据的数据(1个字节)
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 数据从高位到低位
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
void Charge_I2C_SendByte(unsigned char SendByte) 
{
	unsigned char i=8;
	
	Charge_I2C_Delay(3);
	while(i--)
	{
		CHARGE_I2C_SCL_L;
		Charge_I2C_Delay(3);
		if(SendByte&0x80)
			CHARGE_I2C_SDA_H;  
		else 
			CHARGE_I2C_SDA_L;   
		SendByte<<=1;
		Charge_I2C_Delay(1);
		CHARGE_I2C_SCL_H;        //置时钟线为高，通知被控器开始接收数据位
		Charge_I2C_Delay(6);//保证时钟高电平周期大于4u
	}
	CHARGE_I2C_SCL_L;
}
//==================================================================================================
//| 函数名称 | I2C_ReceiveByte 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | 接收1个字节数据
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 接收到的1个字节数据
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 数据从高位到低位
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
unsigned char Charge_I2C_ReceiveByte(void) 
{ 
	unsigned char i=8;
	unsigned char ReceiveByte=0;

	Charge_I2C_Delay(3);
	CHARGE_I2C_SDA_H;     //置数据线为输入方式
	while(i--)
	{
		Charge_I2C_Delay(3);
		ReceiveByte<<=1;      
		CHARGE_I2C_SCL_L;    //置时钟线为低，准备接收数据位
		Charge_I2C_Delay(6); //时钟低电平周期大于4.7u
		CHARGE_I2C_SCL_H;    //置时钟线为高使数据线上数据有效
		Charge_I2C_Delay(3); 
		if(CHARGE_I2C_SDA_READ())//读数据位
		{
			ReceiveByte|=0x01;
		}
	}
	CHARGE_I2C_SCL_L;
	return ReceiveByte;
}

//==================================================================================================
//| 函数名称 | I2C_WriteByte 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | 写入1字节数据
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | SendByte      写入1字节数据
//|          | WriteAddress  待写入数据
//|          | WriteAddress  待写入地址
//|          | DeviceAddress 器件地址
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 0  失败
//|          | 1  成功
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
unsigned char Charge_I2C_WriteByte(unsigned char SendByte, unsigned int WriteAddress, unsigned char DeviceAddress)
{  
    if(!Charge_I2C_Start())
	{
		return 0;
	}
    Charge_I2C_SendByte( DeviceAddress & 0xFE);//写器件地址 
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop(); 
		return 0;
	}
//    I2C_SendByte((unsigned char)((WriteAddress>>8) & 0xFF));   //设置高起始地址      
//    I2C_WaitAck(); 
    Charge_I2C_SendByte((unsigned char)((WriteAddress) & 0xFF));   //设置低起始地址      
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop(); 
		return 0;
	}
    Charge_I2C_SendByte(SendByte);          //写数据
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop(); 
		return 0;
	}  
    Charge_I2C_Stop(); 
	
	//注意：因为这里要等待操作写完，可以采用查询或延时方式(10ms)
    Charge_I2C_Delay(10);
    return 1;
}

//==================================================================================================
//| 函数名称 | I2C_ReceiveByte 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | 读出1字节数据
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | ReadAddress   待读出地址
//|          | DeviceAddress 器件地址
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 读取到的1个字节数据
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
unsigned char Charge_I2C_ReadByte(unsigned int ReadAddress,  unsigned char DeviceAddress, unsigned char* pDat)
{  
    unsigned char temp;
	
    if(!Charge_I2C_Start())
	{
		return 0;
	}

    Charge_I2C_SendByte((DeviceAddress & 0xFE));//写器件地址 
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop();
		return 0;
	}

    Charge_I2C_SendByte((unsigned char)((ReadAddress) & 0xFF));   //设置低起始地址      
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop();
		return 0;
	}
    if(!Charge_I2C_Start())
	{
		return 0;
	}
    Charge_I2C_SendByte((DeviceAddress & 0xFE)|0x01);    //读器件地址
    if(!Charge_I2C_WaitAck())
	{
		Charge_I2C_Stop();
		return 0;
	}
    temp = Charge_I2C_ReceiveByte();
    Charge_I2C_NoAck();    
    Charge_I2C_Stop();
	
	if(pDat != NULL)
	{
		*pDat = temp;
	}	
    return 1;
}


