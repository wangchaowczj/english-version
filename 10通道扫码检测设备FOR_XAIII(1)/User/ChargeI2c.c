//==================================================================================================
void Charge_I2C_Stop(void)
{
	Charge_I2C_Delay(3);
	CHARGE_I2C_SCL_L;
	Charge_I2C_Delay(3);
	CHARGE_I2C_SDA_L; //发送结束条件的数据信号
	Charge_I2C_Delay(3);
	CHARGE_I2C_SCL_H;//发送结束条件的时钟信号
	Charge_I2C_Delay(6);//结束条件建立时间大于4μ
	CHARGE_I2C_SDA_H;//发送I2C总线结束信号
	Charge_I2C_Delay(3);
}

