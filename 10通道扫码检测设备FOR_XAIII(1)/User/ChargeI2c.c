//| 函数名称 | I2C_GPIO_Config 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | I2C IO口配置
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 把SCL SDA都配置为普通IO
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
void Charge_I2C_GPIO_Config(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;

	/***时钟配置***/
	RCC_AHB1PeriphClockCmd(CHARGE_I2C_SCL_PORT_RCC | CHARGE_I2C_SDA_PORT_RCC , ENABLE);
		
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	GPIO_InitStructure.GPIO_OType = GPIO_OType_OD;//
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_NOPULL; 

	/***时钟线SDL, 开漏模式***/
	GPIO_InitStructure.GPIO_Pin = CHARGE_I2C_SCL_PIN; 
	GPIO_Init(CHARGE_I2C_SCL_PORT, &GPIO_InitStructure);
		
	/***数据线SDA, 开漏模式***/
	GPIO_InitStructure.GPIO_Pin = CHARGE_I2C_SDA_PIN; 
	GPIO_Init(CHARGE_I2C_SDA_PORT, &GPIO_InitStructure);	
    
    CHARGE_I2C_SCL_H;
    CHARGE_I2C_SCL_H;
}

