void Charge_I2C_Delay(unsigned int num)
{
	while(num--)
	{
		System168MDelay1us();
	}	
}

//==================================================================================================
//| 函数名称 | I2C_FromBusyToIdle 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | I2C 通过发送9个时钟脉冲信号使总线从死锁状态中恢复
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
void Charge_I2C_FromBusyToIdle(void)
{
    unsigned char i;
    
	Charge_I2C_Delay(6);
    for(i=0; i<9;i++)
    {
        CHARGE_I2C_SCL_L;
        Charge_I2C_Delay(6);//起始条件建立时间大于4.7us延时 
        CHARGE_I2C_SCL_H;
        Charge_I2C_Delay(6);//起始条件建立时间大于4.7us延时        
    }
}
//==================================================================================================
//| 函数名称 | I2C_Delay 
//|----------|--------------------------------------------------------------------------------------
//| 函数功能 | I2C 起始信号
//|----------|--------------------------------------------------------------------------------------
//| 输入参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 返回参数 | 无
//|----------|--------------------------------------------------------------------------------------       
//| 函数设计 | 编写人：郑海    时间：2014-03-01 
//|----------|-------------------------------------------------------------------------------------- 
//|   备注   | 
//|----------|-------------------------------------------------------------------------------------- 
//| 修改记录 | 修改人：          时间：         修改内容： 
//==================================================================================================
unsigned char Charge_I2C_Start(void)
{
	Charge_I2C_Delay(3);
	CHARGE_I2C_SDA_H; //发送起始条件的数据信号
	CHARGE_I2C_SCL_H;
	Charge_I2C_Delay(6);//起始条件建立时间大于4.7us延时
	if(!CHARGE_I2C_SDA_READ())
	{//总线可能已经死锁
		Charge_I2C_FromBusyToIdle();
		if(!CHARGE_I2C_SDA_READ())
		{
			return 0; //SDA线为低电平则总线忙,退出        
		}
	}
	CHARGE_I2C_SDA_L; //发送起始信号
	Charge_I2C_Delay(3);
	if(CHARGE_I2C_SDA_READ())
	{
		return 0; //SDA线为高电平则总线出错,退出
	}	 
	CHARGE_I2C_SCL_L;//锁定I2C总线，准备发送或接收数据
	Charge_I2C_Delay(3);
	return 1;
}
